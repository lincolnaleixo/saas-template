export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const { user, isLoading } = useAuth();
  
  // Check if user is admin
  const isAdmin = user?.email && 
    process.env.NEXT_PUBLIC_ADMIN_ACCESS_EMAILS?.split(',').map(e => e.trim()).includes(user.email);
  
  // Don't show any user data while loading
  const userData = !isLoading && user ? {
    name: user.name || 'User',
    email: user.email,
    avatar: user.avatarUrl || `https://api.dicebear.com/7.x/avataaars/svg?seed=${user.email}`,
  } : {
    name: '',
    email: '',
    avatar: '',
    isLoading: isLoading
  };
  
  // Dynamic navigation based on user role
  const navSecondary = [
    {
      title: "Settings",
      url: "#",
      icon: IconSettings,
    },
    {
      title: "Get Help",
      url: "#",
      icon: IconHelp,
    },
    {
      title: "Search",
      url: "#",
      icon: IconSearch,
    },
    ...(isAdmin ? [{
      title: "Admin Panel",
      url: "/admin",
      icon: IconInnerShadowTop,
    }] : []),
  ];

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <IconInnerShadowTop className="!size-5" />
                <span className="text-base font-semibold">Acme Inc.</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavDocuments items={data.documents} />
        <NavSecondary items={navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={userData} />
      </SidebarFooter>
    </Sidebar>
  )
}
